<?php

namespace app\domains\Report;

/**
 * Интерфейс сервиса для бизнес-логики отчетов
 * 
 * Определяет контракт для операций формирования и обработки отчетов.
 * Реализует паттерн Service для инкапсуляции бизнес-логики создания отчетов,
 * включая агрегацию данных, применение бизнес-правил и форматирование результатов.
 * 
 * @package app\domains\Report
 * @author Zernov Oleg <oi.zernov@gmail.com>
 * @since 1.0
 */
interface ReportServiceInterface
{
    /**
     * Получение отчета по топ авторам за указанный год
     * 
     * Формирует отчет о самых продуктивных авторах за заданный год
     * с применением бизнес-логики фильтрации и форматирования данных.
     * 
     * @param int $year Год для формирования отчета
     * @return array Массив данных отчета с информацией о топ авторах
     */
    public function getTopAuthors(int $year): array;
}